void solver(){
    int n, m; cin>>n>>m;
    multiset<int> ms;
    for(int i = 0; i < n; i++){
        int save; cin>>save;
        ms.insert(save);
    }

    while(m--){
        int lim; cin>>lim;
        auto it = ms.ub(lim);
        if(it == ms.begin()) cout<<-1<<endl;
        else{
            --it;
            cout<<*it<<endl;
            ms.erase(it);
        }
    }
}

