void solver(){
    ll n; cin>>n;
    ll aux = 1, sum = 0;
    while(1){
        if(sum + aux >= n) break;
        sum += aux;
        aux++;
    }

    n -= sum + 1;
    ll num = n, den = aux;

    ll take = __gcd(num, den);
    num /= take;
    den /= take;

    if(den == 1){
        cout<<aux + num<<endl;
    }
    else cout<<aux<<' '<<num<<'/'<<den<<endl;
}

