void solver(){
    int n; ll x; cin>>n>>x;
    ll best = 0;
    for(int i = 0; i < n; i++){
        ll a, b, c; cin>>a>>b>>c;
        x -= a*(b-1LL);
        best = max(best, a*b - c);
    }

    if(x <= 0) cout<<0<<endl;
    else if(!best) cout<<-1<<endl;
    else cout<<(x + best - 1LL) / best<<endl;
}

