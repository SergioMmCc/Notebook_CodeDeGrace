void solver(){
    int n; cin>>n;
    int ans = 0;
    for(int i = 1; i <= n; i++){
        for(int j = 1; j <= n; j++){
            int aux = (i-1)*n + j;
            if(j > 1) aux += (i-1)*n + j - 1;
            if(j < n) aux += (i-1)*n + j + 1;
            if(i > 1) aux += (i-2)*n + j;
            if(i < n) aux += (i)*n + j;
            ans = max(aux, ans);
        }
    }

    cout<<ans<<endl;
}

